public class NY {
    private String ten;

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public NY() {
    }

    public NY(String ten) {
        this.ten = ten;
    }

    @Override
    public String toString() {
        return "NY{" + "ten=" + ten + '}';
    }

    
}
